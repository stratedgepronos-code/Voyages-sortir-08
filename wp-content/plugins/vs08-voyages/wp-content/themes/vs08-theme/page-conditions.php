<h3>5.3 Solde</h3>
<p>Le solde est exigible <strong>30 jours</strong> avant la date de départ, sauf mention contraire sur le contrat de voyage. Pour toute réservation effectuée à moins de 30 jours du départ, le paiement intégral est exigé à la réservation.</p>
